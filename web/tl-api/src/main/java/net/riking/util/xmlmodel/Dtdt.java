package net.riking.util.xmlmodel;

public class Dtdt {
	private String CSNM;
	private String HTDT;
	private String CRCD;
	private String TICD;
	public String getCSNM() {
		return CSNM;
	}
	public void setCSNM(String cSNM) {
		CSNM = cSNM;
	}
	public String getHTDT() {
		return HTDT;
	}
	public void setHTDT(String hTDT) {
		HTDT = hTDT;
	}
	public String getCRCD() {
		return CRCD;
	}
	public void setCRCD(String cRCD) {
		CRCD = cRCD;
	}
	public String getTICD() {
		return TICD;
	}
	public void setTICD(String tICD) {
		TICD = tICD;
	}
	
	
}
