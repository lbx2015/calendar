package net.riking.util.suspxmlmodel;

import java.util.List;

public class Bstr {
	private Rbif RBIF;
	private List<Seif> SEIFs;
	private List<Stif> STIFs;
	public Rbif getRBIF() {
		return RBIF;
	}
	public void setRBIF(Rbif rBIF) {
		RBIF = rBIF;
	}
	public List<Seif> getSEIFs() {
		return SEIFs;
	}
	public void setSEIFs(List<Seif> sEIFs) {
		SEIFs = sEIFs;
	}
	public List<Stif> getSTIFs() {
		return STIFs;
	}
	public void setSTIFs(List<Stif> sTIFs) {
		STIFs = sTIFs;
	}
	
}
